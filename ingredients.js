// ============================================================
// 원료 ↔ 속성 매칭 퀴즈 데이터
// ============================================================
//
// 형식:
//
// ─── 1. 주관식 비율 (파일 상단에 한 줄) ─────────────────────
//
//   # 주관식 비율: 50
//
//   - 0 ~ 100 사이 정수
//   - "*" 표시가 있는 속성 안에서만 적용됨
//   - 0  → 항상 객관식, 100 → 항상 주관식
//   - 기본 50 (절반은 주관식, 절반은 객관식)
//
// ─── 2. 속성 섹션 ───────────────────────────────────────────
//
//   # 속성명           ← 객관식만 출제
//   # 속성명 *         ← 주관식 후보 (위 비율에 따라 일부가 주관식)
//
//   섹션 아래에 원료명을 한 줄에 하나씩 작성.
//
// ─── 3. 속성 그룹핑: 보기를 같은 그룹 안에서만 구성 ───────────
//
//   === 그룹명 ===   로 시작하고
//   === ===          로 끝냄.
//
//   그룹 안의 속성들끼리만 서로 보기에 등장함 (헷갈리는 속성 묶기).
//   그룹 밖의 속성은 기존처럼 전체 풀에서 보기 구성.
//   보기가 4개 미만이면 그룹 밖에서 자동 보충됨.
//
//   예:
//     # 정제수
//     증류수
//
//     === 계면활성제 ===
//     # 양이온 계면활성제
//     세트리모늄브로마이드
//
//     # 음이온 계면활성제
//     소듐라우릴설페이트
//
//     # 비이온 계면활성제
//     폴리소르베이트20
//     === ===
//
//     # 고급지방산 *
//     라우릭애씨드
//
// ─── 4. 같은 원료가 여러 속성에 속할 때 ─────────────────────
//
//   같은 원료명이 여러 섹션에 등장하면 자동으로 다중 속성 처리됨.
//   객관식 보기에는 그 원료의 다른 속성이 동시에 등장하지 않도록 필터링됨.
//   주관식 정답은 그 원료가 속한 속성 중 무엇이든 정답으로 인정됨.
//
// ─── 5. 문제 유형 ───────────────────────────────────────────
//
//   유형 1: 원료명 → 속성(종류) 맞히기
//     - 객관식: 보기 5개 중 1개 정답
//     - 주관식: 텍스트 입력 (위 비율 + "*" 표시에 따라 출제)
//
//   유형 2: 속성(종류) → 원료 맞히기
//     - 항상 객관식 5지선다
//
// ─── 새 항목: 중요도 표시 ★ / ＃ ──────────────────────────────
//
//   속성명 줄 또는 원료명 줄 끝에 기호를 붙여 중요도 표시.
//
//   ★  → 중요 (무조건 외워야 함)
//   ＃  → 포기 가능 (시간 남으면 외우기) ← 주의: 전각 문자 U+FF03
//   없음 → 일반 (기본값)
//
//   속성 줄에 붙이기:
//     # 보습제 ★          ← 이 속성 전체가 "중요"
//     # 색소 ＃            ← 이 속성 전체가 "포기 가능"
//     # 계면활성제 * ★    ← 주관식 후보이면서 중요 (* 뒤에 ★)
//
//   원료 줄에 붙이기:
//     글리세린 ★          ← 이 원료만 "중요"
//     파라벤 ＃            ← 이 원료만 "포기 가능"
//
//   퀴즈 앱에서:
//   - ★ 중요 항목은 "★ 중요" 배지가 문제 카드에 표시됨
//   - ＃ 포기 가능 항목은 "＃ 포기 가능" 배지 표시
//   - "★ 중요만" 필터로 중요 문제만 집중 학습 가능
//   - "＃ 포기 가능 포함" 체크 해제 시 해당 문제 출제에서 제외
//
// ─── 주의사항 ───────────────────────────────────────────────
//
//   - 빈 줄은 무시됨 (가독성을 위해 자유롭게 넣어도 됨)
//   - //로 시작하는 줄은 주석 (데이터에서 제외됨)
//   - "#"는 반드시 줄 맨 앞에서 시작
//   - 속성명·원료명 앞뒤 공백은 자동으로 제거됨
//   - "=== 그룹명 ===" / "=== ===" 줄은 그룹 마커로 처리됨 (데이터가 아님)
//
// ─── 예시 ───────────────────────────────────────────────────
//
//   # 주관식 비율: 50
//
//   # 보습제
//   글리세린
//   부틸렌글라이콜
