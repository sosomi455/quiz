//
//     → 생성되는 문제:
//       ① 정의→용어: "화장품 중에서 피부의 미백, 주름개선 등에..." → 답: 기능성화장품
//       ② 빈칸: [기능성화장품] "...피부의 ___ , 주름개선 등에..." → 답: 미백
